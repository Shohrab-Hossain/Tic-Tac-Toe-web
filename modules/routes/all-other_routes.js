// Node Module
var express  = require('express'),
    router   = express.Router({ mergeParams: true });
    

// |-------------------------------------------------------------------|
// |    INITIATING ROUTE                                               |
// |-------------------------------------------------------------------|

router.get('*', function(req, res){  // this renders error page '404.ejs' for all other pages
    res.render('404');
})


// |-------------------------------------------------------------------|
// |    EXPORTING THE ROUTE                                            |
// |-------------------------------------------------------------------|

module.exports = router;
